package org.oxerr.commons.ws.rs.bean;

public class Bean {

	private String f1;

	private String f2;

	public Bean() {
	}

	public Bean(String f1, String f2) {
		this.f1 = f1;
		this.f2 = f2;
	}

	public String getF1() {
		return f1;
	}

	public void setF1(String f1) {
		this.f1 = f1;
	}

	public String getF2() {
		return f2;
	}

	public void setF2(String f2) {
		this.f2 = f2;
	}

}